package aa.dd;

public class Variable {

	public static void main(String[] args) {
		int a=3;		//변수 a	<===정수
		String b="화면";//변수 b	<===문자열
		
		System.out.println(a);
		System.out.println(b);
		System.out.println("===================");
		System.out.println(b+"출력");
		System.out.println(a+b+"출력");
		
		System.out.println("===================");
		System.out.println();
		
		a=10;
		b="오늘" ;
		System.out.println(a);
		System.out.println(b);
		System.out.println("===================");
		System.out.println(b+"출력");
		System.out.println(a+b+"출력");
		
		a=30 ;
		b="10" ;
		
		
	}

}
